# Q25. Calculate surface area of a cuboid 
# Input: l = 4, b = 3, h = 2 
# Output: Surface Area = ?

lenght = 4
breadth = 3
height = 2
# formulla Total Surface Area of cuboid : 2(lb + bh + hl)
Surface_Area_of_cuboid = 2*(lenght*breadth + breadth*height + height*lenght)
print(f"Surface of Area of cuboid is = {Surface_Area_of_cuboid}")